import java.util.*;

public class Factorial {
    public static void main (String arg[]){
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int m = sc.nextInt();

        for(int i = 0; i< n; i++){
            System.out.println(i);
        }
        for(int j = 0; j < m;j++ ){
            System.out.println(j);
        }
    }
    }

