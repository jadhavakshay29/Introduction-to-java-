public class Operators {
    public static void main(String[] arg){
         int a= 60;
         int b= 20;
         int c= 1;
            // AND oprator
         System.out.println(a&b); 
            // OR operator
         System.out.println(a|b);
           // EXOR operator
         System.out.println(a^b);
           //NOT operator
         System.out.println(~a);
           //NOT formula = -(x+1) 
         System.out.println(~c);
         System.out.println(~(~c));
    }
}
