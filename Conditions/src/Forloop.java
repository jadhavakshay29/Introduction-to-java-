import java.util.*;

public class Forloop {
    public static void main(String arg[]){
     // output will be like each number in next line
        for(int counter = 0; counter <= 10; counter++){
            System.out.println(counter); // we have use println for each next line o/p
        }
        for(int i = 1; i <= 10; i++){
            System.out.print(i+" "); // this will give space between numbers
                                     // we have only used print cause we want o/p in one line
        }
    }
}
